#include<stdio.h>

int main(){
    int age = 25;
    float pi = 3.14;
    double precisePi = 3.14159;
    char grade = 'A';
    _Bool isTrue = 1;

    printf("Age: %d\n", age);
    printf("Pi: %f\n", pi);
    printf("Precise Pi: %lf\n", precisePi);
    printf("Grade: %c\n", grade);
    printf("Is True: %d\n", isTrue);

    return 0;
}